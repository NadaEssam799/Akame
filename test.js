$(document).ready(function(){
    $(".on").mouseenter(function(){
  $(this).css("box-shadow", " 0px 5px 50px 4px rgba(0, 0, 0, 0.3)");
  });
   
    $(".on").mouseleave(function(){
  $(this).css("box-shadow", " 0 0px 0px rgba(0, 0, 0, 0.3)");
  });
});



